package cn.jy.entity;

public class Goodsinfo {
	private String bid;
	private String gid;
	private String gname;
	private String boxid;
	private String boxsize;
	private String boxkind;
	private String leadnumber;
	private int gcount;
	private String gunit;
	private double gtotalweight;
	private double glength;
	private double gheight;
	private double gwidth;
	private double gvolume;

	public String getBid() {
		return bid;
	}

	public void setBid(String bid) {
		this.bid = bid;
	}

	public String getGid() {
		return gid;
	}

	public void setGid(String gid) {
		this.gid = gid;
	}

	public String getGname() {
		return gname;
	}

	public void setGname(String gname) {
		this.gname = gname;
	}

	public String getBoxid() {
		return boxid;
	}

	public void setBoxid(String boxid) {
		this.boxid = boxid;
	}

	public String getBoxsize() {
		return boxsize;
	}

	public void setBoxsize(String boxsize) {
		this.boxsize = boxsize;
	}

	public String getBoxkind() {
		return boxkind;
	}

	public void setBoxkind(String boxkind) {
		this.boxkind = boxkind;
	}

	public String getLeadnumber() {
		return leadnumber;
	}

	public void setLeadnumber(String leadnumber) {
		this.leadnumber = leadnumber;
	}

	public int getGcount() {
		return gcount;
	}

	public void setGcount(int gcount) {
		this.gcount = gcount;
	}

	public String getGunit() {
		return gunit;
	}

	public void setGunit(String gunit) {
		this.gunit = gunit;
	}

	public double getGtotalweight() {
		return gtotalweight;
	}

	public void setGtotalweight(double gtotalweight) {
		this.gtotalweight = gtotalweight;
	}

	public double getGlength() {
		return glength;
	}

	public void setGlength(double glength) {
		this.glength = glength;
	}

	public double getGheight() {
		return gheight;
	}

	public void setGheight(double gheight) {
		this.gheight = gheight;
	}

	public double getGwidth() {
		return gwidth;
	}

	public void setGwidth(double gwidth) {
		this.gwidth = gwidth;
	}

	public double getGvolume() {
		return gvolume;
	}

	public void setGvolume(double gvolume) {
		this.gvolume = gvolume;
	}

	public Goodsinfo(String bid, String gid, String gname, String boxid,
			String boxsize, String boxkind, String leadnumber, int gcount,
			String gunit, double gtotalweight, double glength, double gheight,
			double gwidth, double gvolume) {
		super();
		this.bid = bid;
		this.gid = gid;
		this.gname = gname;
		this.boxid = boxid;
		this.boxsize = boxsize;
		this.boxkind = boxkind;
		this.leadnumber = leadnumber;
		this.gcount = gcount;
		this.gunit = gunit;
		this.gtotalweight = gtotalweight;
		this.glength = glength;
		this.gheight = gheight;
		this.gwidth = gwidth;
		this.gvolume = gvolume;
	}

	public Goodsinfo() {
		super();
	}
	
}