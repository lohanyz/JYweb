package cn.jy.entity;

public class Truckinfo {
	private String tid;
	private double toil01;
	private String wid;
	private String bid;
	public Truckinfo(String tid, double toil01, String wid, String bid) {
		super();
		this.tid = tid;
		this.toil01 = toil01;
		this.wid = wid;
		this.bid = bid;
	}
	public Truckinfo() {
		super();
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public double getToil01() {
		return toil01;
	}
	public void setToil01(double toil01) {
		this.toil01 = toil01;
	}
	public String getWid() {
		return wid;
	}
	public void setWid(String wid) {
		this.wid = wid;
	}
	public String getBid() {
		return bid;
	}
	public void setBid(String bid) {
		this.bid = bid;
	}
	
	
	
}
